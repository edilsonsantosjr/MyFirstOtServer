<h3><b>
Para usar o Fly, basta voce digitar no jogo: !fly ou clica na ferramenta da janela FLY-VOAR e em seguida em FLY-VOAR.
O mesmo segue para desativar o Fly.<br>
Para subir voce pode digitar h1, ou voce pode clicar na ferramenta da janela UP.<br>
Para descer voce pode digitar h2, ou voce pode clicar na ferramenta da janela DOWN.<br>
Tem a op&ccedil;&atilde;o INFO na janela de ferramenta, que pode ser ativada digitando: !infofly
<br><br>
Muito IMPORTANTE lembrar que voce nao pode atacar monstros ou jogadores usando o Fly.<br>
Ao ser atacado por alguem e criar o chamado BATTLE, voce tem que desativar seu FLY para poder atacar o jogador ou monstro agressivo.<br>
Voce N&Atilde;O pode ativar ou desativar seu FLY em locais protegidos(ProtectZone).<br>
O sistema ainda esta em testes, por isso nao ativemos o sistema para ser usado com o modo PVP.<br>
Vamos ativar o FLY ao mesmo tempo do modo PVP, assim que vermos que o sistema foi aceito e esta apto para ser ativo junto ao PVP.<br>

